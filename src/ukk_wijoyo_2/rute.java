    private void tblruteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblruteMouseClicked
        // TODO add your handling code here:
        int row = tblrute.getSelectedRow();
        String diklik = (tblrute.getModel().getValueAt(row, 0).toString());
        String sql = "SELECT * FROM rute WHERE id_rute='"+diklik+"'";

        try {
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();

            if((rs.next())){
                String tambah1 = rs.getString("id_rute");
                id.setText(tambah1);
                String tambah2 = rs.getString("depart_from");
                from.setText(tambah2);
                String tambah3 = rs.getString("depart_to");
                to.setText(tambah3);
                String tambah4 = rs.getString("harga");
                harga.setText(tambah4);
                String tambah5 = rs.getString("nama_transport");
                nama_trans.setText(tambah5);
                String tambah11 = rs.getString("depart_date");
                ((JTextField)depart_date.getDateEditor().getUiComponent()).setText(tambah11);
            }
        } catch (SQLException ex) {
            Logger.getLogger(maskapai.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_tblruteMouseClicked

