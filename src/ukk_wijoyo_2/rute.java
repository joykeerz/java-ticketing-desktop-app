    private void addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addActionPerformed

        try {
            String check = "SELECT * FROM rute WHERE id_rute='"+id.getText()+"'";
            pst = conn.prepareStatement(check);
            rs = pst.executeQuery();
            if((!rs.next())){

                if (!id.getText().isEmpty() && !harga.getText().isEmpty() && !to.getText().isEmpty()){
                    String query = "INSERT INTO rute (id_rute,depart_from,depart_to,harga,nama_transport,depart_date) value(?,?,?,?,?,?)";
                    pst = conn.prepareStatement(query);

                    pst.setString(1, id.getText());
                    pst.setString(2, from.getText());
                    pst.setString(3, to.getText());
                    pst.setString(4, harga.getText());
                    pst.setString(5, nama_trans.getText());
                    pst.setString(6, ((JTextField)depart_date.getDateEditor().getUiComponent()).getText());
                    
                    pst.execute();
                    JOptionPane.showMessageDialog(null, "Data Berhasil  Ditambah");
                    show_data();
                    clear();
                }

            }else{
                JOptionPane.showMessageDialog(null, "Data Sudah ada");
                clear();
            }

        } catch (SQLException ex) {
            Logger.getLogger(maskapai.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_addActionPerformed

